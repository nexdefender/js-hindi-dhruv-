while (i<=10) {
    console.log(`value of i is ${i}`);
    i=i+2
    
}

let myArray = ["ironman","spiderman","thor"]

let arr = 0

while (arr < myArray.length) {
    console.log(`value is ${myArray[arr]}`);
    arr = arr + 1
    
}

let score = 5

do {
    console.log(`score is ${score}`);
    score++;
} while (score<=10);

while (score<=11) {
    console.log(`score is ${score}`);
    score++;
}

